//! Turn planned toolpaths into G-code.
//!
//! Extrusion math: the deposited bead is a **stadium** (flat core, semicircular
//! shoulders — see `config::bead_area_mm2`), so a move of length `L` advances
//! the filament by `E = L * bead_area / filament_cross_section`. Adjacent beads
//! are *placed* at the stadium spacing by the planner, so the shoulders overlap
//! and surfaces come out watertight without over-extrusion.
//!
//! Output targets Klipper: relative extrusion (`M83`) and a retraction before
//! every travel between separate extrusions. The start/end g-code comes from the
//! printer profile (see `config`); `{placeholders}` are substituted here.

use config::Settings;
use gcode::GcodeBuilder;
use geo2d::{Contour, Point, Polygons};
use rayon::prelude::*;

use crate::plan::{LayerPlan, PathKind, ToolPath, Travel};

/// Emit complete G-code for a planned model.
pub fn to_gcode(layers: &[LayerPlan], s: &Settings) -> String {
    let mut g = GcodeBuilder::new();
    let area = s.filament_area_mm2();
    let travel_f = s.travel_speed_mm_s * 60.0;
    let retract_f = s.retract_speed_mm_s * 60.0;
    // Hopped travels lift by this much. With brick layering, force at least enough
    // to clear a lifted bead (top = layer Z + layer height) even if z-hop is off.
    let hop_mm = if s.brick_layers {
        s.z_hop_mm.max(s.layer_height_mm + 0.25)
    } else {
        s.z_hop_mm
    };

    // Cumulative time after each layer — drives M73 progress/remaining.
    let mut cum_secs: Vec<f64> = Vec::with_capacity(layers.len());
    {
        let travel_v = s.travel_speed_mm_s.max(1.0);
        let mut acc = 0.0;
        let mut prev_z = 0.0;
        for layer in layers {
            acc += (layer.print_z_mm - prev_z).abs() / travel_v;
            prev_z = layer.print_z_mm;
            acc += layer_print_seconds(layer, s, layer.speed_scale);
            cum_secs.push(acc);
        }
    }
    let total_secs = cum_secs.last().copied().unwrap_or(0.0);
    let (fil_mm, fil_g) = estimate_filament(layers, s);

    g.comment("generated by slicer");
    g.comment(&format!("estimated printing time = {}", format_duration(total_secs)));
    g.comment(&format!("filament used [mm] = {fil_mm:.1}"));
    g.comment(&format!("filament used [g] = {fil_g:.1}"));
    g.comment(&format!("total layers = {}", layers.len()));
    if s.max_volumetric_speed_mm3_s > 0.0 {
        g.comment(&format!("flow limit = {:.1} mm3/s", s.max_volumetric_speed_mm3_s));
        for (kind, nominal, clamped) in audit_flow_clamps(layers, s) {
            g.comment(&format!(
                "flow-limited: {} {:.0} -> {:.0} mm/s",
                kind_label(kind),
                nominal,
                clamped
            ));
        }
    }
    if s.heat_control {
        let cap = s.max_heat_mw_mm2;
        match audit_smoothing(layers, s) {
            Some(r) => g.comment(&format!(
                "heat control: cap {cap:.1} mW/mm2, transitions <= {:.1}%/layer ({:.0}% time budget)",
                r.pct_per_layer, s.smooth_extra_time_pct
            )),
            None => g.comment(&format!("heat control: cap {cap:.1} mW/mm2")),
        }
        for z in audit_heat_control_temp(layers, s) {
            g.comment(&format!(
                "heat control (temp): layers {}-{} -> {:.0}C",
                z.first_layer, z.last_layer, z.temp_c
            ));
        }
        for r in audit_heat_control_speed(layers, s) {
            let dwell = if r.dwell_s > 0.05 { format!(" + {:.1}s dwell", r.dwell_s) } else { String::new() };
            g.comment(&format!(
                "heat control (speed): layers {}-{} slowed to {:.0}%{dwell}{}",
                r.first_layer,
                r.last_layer,
                r.worst_scale * 100.0,
                if r.floor_hit { " (still hot at the cap)" } else { "" }
            ));
        }
    }
    g.comment(&format!(
        "layer_h={} line_w={} walls={} top/bot={}/{} infill={} ({}) bed={}x{}",
        s.layer_height_mm,
        s.line_width_mm,
        s.wall_count,
        s.top_layers,
        s.bottom_layers,
        s.infill_density,
        s.sparse_pattern.label(),
        s.bed_size_x_mm,
        s.bed_size_y_mm
    ));

    // Machine state we depend on, then the printer's start sequence (heating /
    // homing / PRINT_START macro).
    g.raw("G21"); // millimeters
    g.raw("G90"); // absolute XYZ
    g.raw("M83"); // relative extrusion (Klipper-recommended)
    emit_template(&mut g, &s.start_gcode, s);
    // Motion limits, set after PRINT_START so our values win. M204 S is understood
    // by Klipper and Marlin; SQUARE_CORNER_VELOCITY is Klipper's "jerk" equivalent.
    // Acceleration then follows the feature being printed (M204 per change).
    let mut cur_accel = s.first_layer_accel_mm_s2;
    g.raw(&format!("M204 S{cur_accel:.0}"));
    g.raw(&format!("SET_VELOCITY_LIMIT SQUARE_CORNER_VELOCITY={:.1}", s.jerk_mm_s));
    if s.pressure_advance > 0.0 {
        g.raw(&format!("SET_PRESSURE_ADVANCE ADVANCE={:.4}", s.pressure_advance));
    }
    g.fan(0);
    let mut cur_fan = 0u32;

    let fan_duty = |frac: f64| (frac.clamp(0.0, 1.0) * 255.0).round() as u32;
    // Extra fans, gated on the printer declaring them — vanilla Klipper/Marlin
    // reads the M106 P-form as the primary fan. The aux part fan (M106 P2)
    // follows the part fan's first-layers gate in the loop below; the chamber
    // exhaust (M106 P3) runs flat for the whole print. Both shut off at the end.
    let mut cur_aux_fan = 0u32;
    if s.has_aux_fan {
        g.raw("M106 P2 S0");
    }
    if s.has_exhaust_fan && s.exhaust_fan_speed > 0.0 {
        g.raw(&format!("M106 P3 S{}", fan_duty(s.exhaust_fan_speed)));
    }
    let first_spiral = s.bottom_layers; // vase mode: first continuously-rising layer
    // Tail of the most recently printed path, for wiping the nozzle back over
    // it when the next travel retracts. Carried across layers: a layer-change
    // wipe runs one layer height above the bead, which still releases the
    // pressure while moving instead of parked on the seam.
    let mut wipe_tail: Option<Vec<Point>> = None;

    for layer in layers {
        g.comment(&format!("LAYER {} z={:.3}", layer.index, layer.print_z_mm));
        // Time-based progress for the printer's display.
        let done = if layer.index == 0 { 0.0 } else { cum_secs[layer.index - 1] };
        if total_secs > 0.0 {
            g.raw(&format!(
                "M73 P{:.0} R{:.0}",
                (done / total_secs * 100.0).min(100.0),
                ((total_secs - done) / 60.0).ceil()
            ));
        }
        // Temperature: the temp schedule's setpoints win; otherwise the
        // one-shot drop from first-layer temp to printing temp. M104 either
        // way — set without waiting, the schedule already planned the leads.
        if let Some(t) = layer.temp_command_c {
            g.raw(&format!("M104 S{t:.0}"));
        } else if layer.index == 1 && s.first_layer_nozzle_temp_c != s.nozzle_temp_c {
            g.raw(&format!("M104 S{}", s.nozzle_temp_c));
        }
        // Part cooling: off for the first `fan_off_layers`, then the normal duty;
        // bridges may override per path below.
        let normal_fan = if layer.index < s.fan_off_layers { 0 } else { fan_duty(s.fan_speed) };
        if normal_fan != cur_fan {
            g.fan(normal_fan);
            cur_fan = normal_fan;
        }
        if s.has_aux_fan {
            let aux = if layer.index < s.fan_off_layers { 0 } else { fan_duty(s.aux_fan_speed) };
            if aux != cur_aux_fan {
                g.raw(&format!("M106 P2 S{aux}"));
                cur_aux_fan = aux;
            }
        }

        // Spiral vase: above the solid bottom, the single wall loop climbs
        // continuously from the previous layer's top to this one's.
        if s.spiral_vase && layer.index >= first_spiral {
            if let Some(path) = spiral_loop(layer) {
                let feed = feed_for(path, layer.index, layer.height_mm, layer_flow_cap_mm3_s(layer, s), s)
                    * layer.speed_scale;
                let coeff = config::bead_area_mm2(path.width_mm, layer.height_mm * path.height_scale) / area
                    * flow_factor(path, s);
                g.raw(&format!(";TYPE:{}", type_label(path.kind)));
                let accel = accel_for(path.kind, layer.index, s);
                if (accel - cur_accel).abs() > 0.5 {
                    g.raw(&format!("M204 S{accel:.0}"));
                    cur_accel = accel;
                }
                emit_spiral_layer(&mut g, layer, path, coeff, feed, travel_f, retract_f, s);
                continue;
            }
        }

        g.move_z(layer.print_z_mm, travel_f);
        let mut cur_z = layer.print_z_mm;

        let mut cur_type: Option<&'static str> = None;
        for (i, path) in layer.paths.iter().enumerate() {
            if path.points.len() < 2 {
                continue;
            }
            // Feature annotation + acceleration, on change. The accel is set
            // before the lead-in travel so the whole block moves as one.
            let t = type_label(path.kind);
            if cur_type != Some(t) {
                g.raw(&format!(";TYPE:{t}"));
                cur_type = Some(t);
            }
            let accel = accel_for(path.kind, layer.index, s);
            if (accel - cur_accel).abs() > 0.5 {
                g.raw(&format!("M204 S{accel:.0}"));
                cur_accel = accel;
            }
            // Bridges and overhanging walls want max cooling; everything else
            // the layer's normal duty.
            let airborne = matches!(
                path.kind,
                PathKind::Bridge
                    | PathKind::ArcOverhang
                    | PathKind::InternalBridge
                    | PathKind::OverhangWall
                    | PathKind::BottomSkin
            );
            let want_fan = if airborne && layer.index >= s.fan_off_layers {
                fan_duty(s.bridge_fan_speed).max(normal_fan)
            } else {
                normal_fan
            };
            if want_fan != cur_fan {
                g.fan(want_fan);
                cur_fan = want_fan;
            }

            // Brick-layered perimeters print half a layer up and over-extrude a touch.
            let z = layer.print_z_mm + path.z_offset_mm;
            let feed =
                feed_for(path, layer.index, layer.height_mm, layer_flow_cap_mm3_s(layer, s), s)
                    * layer.speed_scale
                    * path_scale(layer, i);
            let coeff = config::bead_area_mm2(path.width_mm, layer.height_mm * path.height_scale) / area
                * flow_factor(path, s);
            let start = path.points[0];

            // The travel (combed route, or a retracted/z-hopped hop over a void)
            // was planned in `plan_travels` — replay it, at this path's Z.
            let tr = &layer.travels[i];
            if tr.retract && s.retract_len_mm > 0.0 {
                g.retract(s.retract_len_mm, retract_f);
                // Wipe back along the printed bead: the pressure-release ooze
                // smears onto plastic instead of blobbing the seam.
                if s.wipe_mm > 0.0 {
                    if let Some(tail) = &wipe_tail {
                        for p in tail {
                            g.travel(p.x_mm(), p.y_mm(), travel_f);
                        }
                    }
                }
            }
            if tr.hop && hop_mm > 0.0 {
                g.move_z(z + hop_mm, travel_f);
            } else if (z - cur_z).abs() > 1.0e-9 {
                g.move_z(z, travel_f);
            }
            for pt in &tr.points {
                g.travel(pt.x_mm(), pt.y_mm(), travel_f);
            }
            if tr.hop && hop_mm > 0.0 {
                g.move_z(z, travel_f);
            }
            cur_z = z;
            if tr.retract && s.retract_len_mm > 0.0 {
                g.unretract(s.retract_len_mm, retract_f);
            }

            if let Some(ws) = &path.widths {
                // Variable-width bead: per-segment E from the mean of the two
                // endpoint widths (no arc fitting — E varies along the path).
                let n_pts = path.points.len();
                let segs = if path.closed { n_pts } else { n_pts - 1 };
                for k in 0..segs {
                    let a = path.points[k];
                    let b = path.points[(k + 1) % n_pts];
                    let w = 0.5 * (ws[k] + ws[(k + 1) % n_pts]);
                    let c = config::bead_area_mm2(w, layer.height_mm * path.height_scale) / area
                        * flow_factor(path, s);
                    g.extrude(b.x_mm(), b.y_mm(), dist_mm(a, b) * c, feed);
                }
            } else if s.arc_fitting {
                emit_arcs(&mut g, &path.points, path.closed, coeff, feed, s.arc_tolerance_mm);
            } else {
                let mut prev = start;
                for &p in &path.points[1..] {
                    g.extrude(p.x_mm(), p.y_mm(), dist_mm(prev, p) * coeff, feed);
                    prev = p;
                }
                if path.closed {
                    g.extrude(start.x_mm(), start.y_mm(), dist_mm(prev, start) * coeff, feed);
                }
            }
            wipe_tail = compute_wipe_tail(path, s.wipe_mm);
        }

        // Park-and-wait cooling dwell: the speed lever banked seconds here when
        // an island was over the heat target with every path already at the
        // speed floor. Spend them retracted, lifted clear of the part, and —
        // when the layer has sparse infill — parked over it, so ooze shed
        // during the wait lands on beads the following layers bury instead of
        // over the layer's visible endpoint. Park and return both happen
        // lifted, and the nozzle comes back to where it stopped so the next
        // layer's planned (combed) travel still starts from the endpoint it
        // was derived from.
        if layer.dwell_s > 0.0 {
            let back = layer.paths.iter().rev().find(|p| p.points.len() >= 2).map(path_end);
            let park = match (dwell_park_point(layer), back) {
                (Some(p), Some(b)) if dist_mm(p, b) > 1.0 => Some((p, b)),
                _ => None,
            };
            if s.retract_len_mm > 0.0 {
                g.retract(s.retract_len_mm, retract_f);
            }
            g.move_z((layer.print_z_mm + DWELL_LIFT_MM).min(s.bed_size_z_mm.max(layer.print_z_mm)), travel_f);
            if let Some((p, _)) = park {
                g.travel(p.x_mm(), p.y_mm(), travel_f);
            }
            g.raw(&format!("G4 P{:.0}", layer.dwell_s * 1000.0));
            if let Some((_, b)) = park {
                g.travel(b.x_mm(), b.y_mm(), travel_f);
            }
            g.move_z(layer.print_z_mm, travel_f);
            if s.retract_len_mm > 0.0 {
                g.unretract(s.retract_len_mm, retract_f);
            }
        }
    }

    if s.retract_len_mm > 0.0 {
        g.retract(s.retract_len_mm, retract_f);
    }
    if total_secs > 0.0 {
        g.raw("M73 P100 R0");
    }
    // Leave no P-addressed fan running: end g-code macros handle the primary
    // fan (M107) but don't know about these.
    if cur_aux_fan != 0 {
        g.raw("M106 P2 S0");
    }
    if s.has_exhaust_fan && s.exhaust_fan_speed > 0.0 {
        g.raw("M106 P3 S0");
    }
    emit_template(&mut g, &s.end_gcode, s);

    g.finish()
}

/// The single closed external-perimeter loop of a vase layer, if the layer is
/// spiralable (exactly one printable path of that shape).
fn spiral_loop(layer: &LayerPlan) -> Option<&ToolPath> {
    let mut it = layer.paths.iter().filter(|p| p.points.len() >= 2);
    match (it.next(), it.next()) {
        (Some(p), None) if p.closed && p.kind == PathKind::ExternalPerimeter => Some(p),
        _ => None,
    }
}

/// Print one vase layer: walk the loop once, ramping Z linearly with the
/// distance traveled so the wall rises in one continuous helix (no seam, no
/// layer-change retraction).
#[allow(clippy::too_many_arguments)]
fn emit_spiral_layer(
    g: &mut GcodeBuilder,
    layer: &LayerPlan,
    path: &ToolPath,
    coeff: f64,
    feed: f64,
    travel_f: f64,
    retract_f: f64,
    s: &Settings,
) {
    let z_top = layer.print_z_mm;
    let z_bot = z_top - layer.height_mm;
    // Reach the loop start (real travel only on the first spiral layer — after
    // that the nozzle is already at the loop's start/end point).
    if let Some(tr) = layer.travels.first() {
        if !tr.points.is_empty() {
            if tr.retract && s.retract_len_mm > 0.0 {
                g.retract(s.retract_len_mm, retract_f);
            }
            for pt in &tr.points {
                g.travel(pt.x_mm(), pt.y_mm(), travel_f);
            }
            if tr.retract && s.retract_len_mm > 0.0 {
                g.unretract(s.retract_len_mm, retract_f);
            }
        }
    }
    let pts = &path.points;
    let n = pts.len();
    let total: f64 = (0..n).map(|k| dist_mm(pts[k], pts[(k + 1) % n])).sum();
    if total < 1.0e-6 {
        return;
    }
    let mut cum = 0.0;
    for k in 1..=n {
        let a = pts[k - 1];
        let b = pts[k % n];
        let d = dist_mm(a, b);
        if d < 1.0e-9 {
            continue;
        }
        cum += d;
        let z = z_bot + (z_top - z_bot) * (cum / total);
        g.extrude_z(b.x_mm(), b.y_mm(), z, d * coeff, feed);
    }
}

/// Substitute `{placeholders}` in a template and emit it line by line.
fn emit_template(g: &mut GcodeBuilder, template: &str, s: &Settings) {
    let text = substitute(template, s);
    for line in text.lines() {
        if !line.trim().is_empty() {
            g.raw(line.trim_end());
        }
    }
}

fn substitute(template: &str, s: &Settings) -> String {
    template
        .replace("{nozzle_temp}", &s.nozzle_temp_c.to_string())
        .replace("{first_layer_nozzle_temp}", &s.first_layer_nozzle_temp_c.to_string())
        .replace("{bed_temp}", &s.bed_temp_c.to_string())
        .replace("{bed_x}", &format!("{:.3}", s.bed_size_x_mm))
        .replace("{bed_y}", &format!("{:.3}", s.bed_size_y_mm))
        .replace("{bed_z}", &format!("{:.3}", s.bed_size_z_mm))
        .replace("{layer_height}", &format!("{:.3}", s.layer_height_mm))
        .replace("{first_layer_height}", &format!("{:.3}", s.first_layer_height_mm))
        .replace("{nozzle_diameter}", &format!("{:.3}", s.nozzle_diameter_mm))
}

/// The configured speed (mm/s) for a feature, before any limits.
fn nominal_speed_mm_s(kind: PathKind, layer_index: usize, s: &Settings) -> f64 {
    if layer_index == 0 {
        return s.first_layer_speed_mm_s; // first layer is slow everywhere
    }
    match kind {
        PathKind::ExternalPerimeter => s.external_perimeter_speed_mm_s,
        PathKind::Solid => s.solid_speed_mm_s,
        // Visible skins share the outer wall's pace: finish over time.
        PathKind::TopSkin | PathKind::BottomSkin => s.external_perimeter_speed_mm_s,
        PathKind::GapFill => s.gap_fill_speed_mm_s,
        PathKind::Ironing => s.ironing_speed_mm_s,
        PathKind::Support => s.support_speed_mm_s,
        // Bridges print into air anchored on both ends.
        PathKind::Bridge => s.bridge_speed_mm_s,
        // Arc overhangs cantilever sideways off the previous ring while
        // unsupported along their whole length — far more delicate than a
        // bridge: ~30% of bridge speed, held in the 5–15 mm/s window the
        // technique is known to work in.
        PathKind::ArcOverhang => (s.bridge_speed_mm_s * 0.3).clamp(5.0, 15.0),
        // Internal bridges (solid over sparse) are anchored every infill cell:
        // the free span is spacing/density, not a real bridge's max span. Sag
        // scales with span, so scale the bridge speed by the span ratio —
        // never slower than a bridge, never faster than solid fill.
        PathKind::InternalBridge => {
            let spacing = config::bead_spacing_mm(s.line_width_mm, s.layer_height_mm);
            let cell = if s.infill_density > 0.0 { spacing / s.infill_density } else { f64::MAX };
            let ratio = (s.max_bridge_span_mm / cell).max(1.0);
            (s.bridge_speed_mm_s * ratio).min(s.solid_speed_mm_s)
        }
        // Wall stretches past the layer below: same physics as bridges.
        PathKind::OverhangWall => s.overhang_speed_mm_s,
        // Skirt is layer-0 only (handled above); listed for exhaustiveness.
        PathKind::Skirt | PathKind::Perimeter | PathKind::Infill => s.print_speed_mm_s,
    }
}

/// Extrusion-flow factor for a path beyond its w×h geometry: per-path flow
/// (brick, ironing), per-kind flow (bridges), and the global multiplier.
fn flow_factor(path: &ToolPath, s: &Settings) -> f64 {
    let kind_flow = if matches!(path.kind, PathKind::Bridge | PathKind::ArcOverhang) { s.bridge_flow } else { 1.0 };
    path.flow * kind_flow * s.extrusion_multiplier
}

/// The nozzle temperature planned for a layer: the temp schedule
/// when present, else the first-layer / profile temperature.
fn layer_nozzle_c(layer: &LayerPlan, s: &Settings) -> f64 {
    layer.planned_temp_c.unwrap_or(if layer.index == 0 {
        s.first_layer_nozzle_temp_c as f64
    } else {
        s.nozzle_temp_c as f64
    })
}

/// The melt ceiling in force on a layer (mm³/s; 0 = unlimited): the profile
/// cap at the profile temperature, derated linearly when the temp schedule
/// runs the layer cooler. Never raised on warmer layers — the profile cap is
/// the calibrated number.
fn layer_flow_cap_mm3_s(layer: &LayerPlan, s: &Settings) -> f64 {
    let cap = s.max_volumetric_speed_mm3_s;
    if cap <= 0.0 {
        return 0.0;
    }
    let below = s.nozzle_temp_c as f64 - layer_nozzle_c(layer, s);
    if below > 0.0 {
        (cap - s.max_flow_derate_per_c.max(0.0) * below).max(1.0)
    } else {
        cap
    }
}

/// Feed rate (mm/min) for a path: the per-feature speed, clamped so the
/// volumetric flow `width × height × speed × flow` never exceeds the layer's
/// melt-rate ceiling (`flow_cap_mm3_s`, 0 = unlimited — pass
/// `layer_flow_cap_mm3_s` so the temp schedule's derate applies). One
/// function feeds the g-code, the time estimate, and the min-layer-time pass,
/// so they always agree.
fn feed_for(
    path: &ToolPath,
    layer_index: usize,
    layer_height_mm: f64,
    flow_cap_mm3_s: f64,
    s: &Settings,
) -> f64 {
    let mut v = nominal_speed_mm_s(path.kind, layer_index, s);
    if flow_cap_mm3_s > 0.0 {
        let mm3_per_mm =
            config::bead_area_mm2(path.width_mm, layer_height_mm * path.height_scale) * flow_factor(path, s);
        if mm3_per_mm > 1.0e-9 {
            v = v.min(flow_cap_mm3_s / mm3_per_mm);
        }
    }
    v * 60.0
}

/// Where the flow ceiling bites: per feature kind (skipping the first layer),
/// the nominal speed and the worst (slowest) clamped speed, for kinds where
/// the clamp actually engaged. Drives the loud reporting in the g-code
/// header, the CLI summary, and the GUI status line.
pub fn audit_flow_clamps(layers: &[LayerPlan], s: &Settings) -> Vec<(PathKind, f64, f64)> {
    use std::collections::BTreeMap;
    let mut worst: BTreeMap<&'static str, (PathKind, f64, f64)> = BTreeMap::new();
    for layer in layers {
        if layer.index == 0 {
            continue; // first layer is slowed anyway
        }
        for path in &layer.paths {
            if path.points.len() < 2 {
                continue;
            }
            let nominal = nominal_speed_mm_s(path.kind, layer.index, s);
            let clamped = feed_for(path, layer.index, layer.height_mm, layer_flow_cap_mm3_s(layer, s), s) / 60.0;
            if clamped < nominal - 1.0e-6 {
                worst
                    .entry(kind_label(path.kind))
                    .and_modify(|e| e.2 = e.2.min(clamped))
                    .or_insert((path.kind, nominal, clamped));
            }
        }
    }
    worst.into_values().collect()
}

