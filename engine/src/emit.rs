//! Turn planned toolpaths into G-code.
//!
//! Extrusion math: the deposited bead approximates a `width × height` rectangle,
//! so a move of length `L` advances the filament by
//! `E = L * width * height / filament_cross_section`.
//!
//! Output targets Klipper: relative extrusion (`M83`) and a retraction before
//! every travel between separate extrusions. The start/end sequences are a
//! generic-Marlin-compatible placeholder (these M-codes work on Klipper too);
//! real per-printer templates / `PRINT_START` macros arrive with the profile
//! system.

use config::Settings;
use gcode::GcodeBuilder;
use geo2d::Point;

use crate::plan::{LayerPlan, PathKind};

/// Emit complete G-code for a planned model.
pub fn to_gcode(layers: &[LayerPlan], s: &Settings) -> String {
    let mut g = GcodeBuilder::new();
    let area = s.filament_area_mm2();
    let travel_f = s.travel_speed_mm_s * 60.0;
    let retract_f = s.retract_speed_mm_s * 60.0;

    // ---- start g-code (generic, Klipper-compatible — tune per printer) ----
    g.comment("generated by slicer - generic profile, tune per printer");
    g.comment(&format!(
        "layer_h={} line_w={} walls={} top/bot={}/{} infill={} bed={}x{}",
        s.layer_height_mm,
        s.line_width_mm,
        s.wall_count,
        s.top_layers,
        s.bottom_layers,
        s.infill_density,
        s.bed_size_x_mm,
        s.bed_size_y_mm
    ));
    g.set_bed_temp(s.bed_temp_c, false);
    g.set_nozzle_temp(s.nozzle_temp_c, false);
    g.set_bed_temp(s.bed_temp_c, true);
    g.set_nozzle_temp(s.nozzle_temp_c, true);
    g.raw("G21"); // millimeters
    g.raw("G90"); // absolute XYZ
    g.raw("M83"); // relative extrusion (Klipper-recommended)
    g.raw("G28"); // home all axes
    g.fan(0);

    for layer in layers {
        g.comment(&format!("LAYER {} z={:.3}", layer.index, layer.print_z_mm));
        if layer.index == 1 {
            g.fan(255); // part cooling on after the first layer
        }
        g.move_z(layer.print_z_mm, travel_f);

        let mut printed = false;
        for path in &layer.paths {
            if path.points.len() < 2 {
                continue;
            }
            let feed = feed_for(path.kind, layer.index, s);
            let coeff = path.width_mm * layer.height_mm / area;
            let start = path.points[0];

            // Retract across the gap between separate extrusions.
            let retract = printed && s.retract_len_mm > 0.0;
            if retract {
                g.retract(s.retract_len_mm, retract_f);
            }
            g.travel(start.x_mm(), start.y_mm(), travel_f);
            if retract {
                g.unretract(s.retract_len_mm, retract_f);
            }

            let mut prev = start;
            for &p in &path.points[1..] {
                g.extrude(p.x_mm(), p.y_mm(), dist_mm(prev, p) * coeff, feed);
                prev = p;
            }
            if path.closed {
                g.extrude(start.x_mm(), start.y_mm(), dist_mm(prev, start) * coeff, feed);
            }
            printed = true;
        }
    }

    // ---- end g-code ----
    if s.retract_len_mm > 0.0 {
        g.retract(s.retract_len_mm, retract_f);
    }
    g.set_nozzle_temp(0, false);
    g.set_bed_temp(0, false);
    g.fan(0);
    g.raw("G91"); // relative
    g.raw("G1 Z5 F600"); // lift
    g.raw("G90"); // absolute
    g.raw("M84"); // disable steppers

    g.finish()
}

/// Feed rate (mm/min): first layer slow everywhere; otherwise the external
/// perimeter is slowed for surface quality.
fn feed_for(kind: PathKind, layer_index: usize, s: &Settings) -> f64 {
    if layer_index == 0 {
        return s.first_layer_speed_mm_s * 60.0;
    }
    match kind {
        PathKind::ExternalPerimeter => s.print_speed_mm_s * 0.5 * 60.0,
        PathKind::Perimeter | PathKind::Solid | PathKind::Infill => s.print_speed_mm_s * 60.0,
    }
}

fn dist_mm(a: Point, b: Point) -> f64 {
    let dx = a.x_mm() - b.x_mm();
    let dy = a.y_mm() - b.y_mm();
    (dx * dx + dy * dy).sqrt()
}

#[cfg(test)]
mod tests {
    use crate::{generate, to_gcode};
    use config::Settings;

    #[test]
    fn gcode_is_klipper_relative_with_retraction() {
        let m = mesh::Mesh::cube(20.0);
        let s = Settings::default();
        let g = to_gcode(&generate(&m, &s), &s);

        assert!(g.contains("M83"), "relative extrusion mode");
        assert!(g.contains("G28"), "homes");
        assert!(g.contains("M104 S200"), "sets nozzle temp");
        assert!(g.contains("M140 S60"), "sets bed temp");
        assert!(g.contains("M84"), "disables steppers at end");
        assert!(
            g.lines().any(|l| l.starts_with("G1 X") && l.contains(" E")),
            "has extruding moves"
        );
        assert!(g.lines().any(|l| l.starts_with("G1 E-")), "has retraction moves");
    }
}
